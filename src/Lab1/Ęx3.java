public class Ęx3 {
    public static void main(String[] args) {
        int[] array = {10, 2, 3, 4, 5};  // Przykładowa tablica

        if (array[0] == 10 || array[array.length - 1] == 10) {
            System.out.println("Tablica zawiera 10 na początku lub na końcu.");
        } else {
            System.out.println("Tablica nie zawiera 10 na początku ani na końcu.");
        }
    }
}
